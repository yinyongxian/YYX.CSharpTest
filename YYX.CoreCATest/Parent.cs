namespace YYX.CoreCATest
{
    public class Parent
    {
        private string name;
        private int age;

        public Parent(string name, int age)
        {
            this.name = name;
            this.age = age;
        }

        public string Name
        {
            get { return name; }
            set { name = value; }
        }

        public int Age
        {
            get { return age; }
            set { age = value; }
        }
    }
}
